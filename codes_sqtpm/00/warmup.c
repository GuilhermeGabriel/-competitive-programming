#include <stdio.h>

int main(){

  // Criação das variáveis a, b e x
  int a, b, x;

  // Leitura do input de a e b
  scanf("%d", &a);
  scanf("%d", &b);

  // Atribuição da soma entre a e b na variável x
  x = a + b;

  // Saída formatada do valor de x
  printf("X = %d\n", x);

  return 0;
}